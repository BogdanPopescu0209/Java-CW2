package charityRuns.business;

public class Competitor {

    private String name;
    private int age;

    public Competitor(String competitorName, int competitorAge) {

        this.name = competitorName;
        this.age = competitorAge;

    }

    public String getName() {

        return name;

    }

    public int getAge() {

        return age;

    }

}
